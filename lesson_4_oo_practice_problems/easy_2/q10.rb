# What are the benefits of using Object Oriented Programming in Ruby?

# -Keeps code maintainable
# -Reduces dependencies within programs
# -Provides opportunity for more reusability and flexibility
# -Lets programmers think at a higher level of abstraction to break down and solve more complex coding problems
# -Lets programmers represent objects as real-world nouns, which helps them conceptualize them more easily and give them methods that clearly describe real-world behaviors
# -Allows for encapsulation so that functionality is only available to parts of the program that need it